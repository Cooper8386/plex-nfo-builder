"""Filesystem watcher (v0.12.0).

Background service that watches every enabled library root with the
``watchdog`` library and triggers the scan → match → build pipeline when
new media lands on disk. Designed to be reliable in the face of partial
imports (Sonarr/Radarr copying multi-gigabyte files) and noisy file events
generated by metadata-aware downloaders.

Design notes
------------
* **Triggers**: directory creation/move events under any library root, plus
  new media-file appearances inside an already-tracked show folder. Both
  collapse to the same debounce key (the immediate child of the library
  root, i.e. the show or movie folder).
* **Debounce**: a configurable per-folder timer (default 30s, env var
  ``WATCHER_DEBOUNCE_SECONDS``, user-overridable from Settings → Watcher).
  Every fresh event for the same folder resets the timer so a 30-minute
  multi-file copy is treated as a single "show settled" event.
* **Stability check**: right before dispatch, the watcher snapshots the
  recursive size+mtime of the folder and waits one short interval to
  confirm nothing changed. If files are still being written, the timer
  is rescheduled instead of firing.
* **Pipeline**: ``scan_library`` → ``auto_match_*`` (respecting per-library
  metadata source) → ``build_svc.start_build``. If the match fails or
  scores below the user's ``auto_match_threshold``, the folder is queued
  in ``watcher_review`` (db) instead of silently skipped.
* **Activity**: every state transition is appended to an in-memory ring
  buffer surfaced at ``GET /api/watcher/events`` so the dedicated
  Watcher page can render a live timeline.
* **Hot reload**: ``reload()`` re-reads the active settings and the
  current library set, then re-arms watches accordingly. Called by the
  settings + library routes so toggling the watcher or enabling a new
  library takes effect immediately, no restart needed.
"""
